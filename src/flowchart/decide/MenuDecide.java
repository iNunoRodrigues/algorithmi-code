    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        try {
            BreakMarks.checkParentesis(txtExpressionShape.getInstruction());
            decision.buildInstruction(txtExpressionShape.getInstruction(), txtComments.getText());
            decision.parseShape();
            decision.setSelected(false);
            setVisible(false);
        } catch (FlowchartException e) {
            e.setInstruction(txtExpressionShape.getInstruction());
            Fdialog.compileException(e);
        }
    }//GEN-LAST:event_btOkActionPerformed

