    @Override
    public void showDialog(Fshape shape, int x, int y) {

        
        if (shape instanceof IfThenElse) {
            INSTRUCTION = "IF";
            txtExpressionShape.setKeyword(IfThenElse.KEYWORD);
            this.decision = (IfThenElse) shape;
        } else if (shape instanceof While_Do) {
            INSTRUCTION = "WHILE";
            txtExpressionShape.setKeyword(While_Do.KEYWORD);
            this.decision = (While_Do) shape;
        } else if (shape instanceof Do_While) {
            INSTRUCTION = "DO";
            txtExpressionShape.setKeyword(Do_While.KEYWORD);
            this.decision = (Do_While) shape;
        }
        setTitle(Fi18N.get(INSTRUCTION + ".instruction.title"));
        Fi18N.loadMenuShapeHelp(lblHelp, INSTRUCTION + ".instruction");
        //Fi18N.loadTabTile(tabMain, INSTRUCTION + ".tab.instruction", 0);

        oldText = shape.getInstruction();
        memory = shape.algorithm.getMemory(shape.parent);
        
        txtExpressionShape.updateMenu(memory, shape.algorithm.getMyProgram());
        //-----------------------------------------
        if (this.decision.getInstruction().isEmpty()) {
            txtExpressionShape.setInstruction(" ");
            //txtExpressionShape.setInstruction(FkeyWord.get("TYPE.boolean.true"));
        } else {
            txtExpressionShape.setInstruction(this.decision.getInstruction());
        }
        txtComments.setText(this.decision.getComments());
        //-----------------------------------------
        //:::::::::::::::::::::::::::::::::::::::::
        //::::::::   Show Dialog  :::::::::::::::::
        setLocationRelativeTo(null);
        updateFont();
        this.setVisible(true);
        txtExpressionShape.requestFocus();
        //:::::::::::::::::::::::::::::::::::::::::
    }

